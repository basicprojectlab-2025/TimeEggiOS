import Foundation

/*
 Google Maps SDK를 사용하기 위해 다음 단계를 따라주세요:

 1. Xcode에서 프로젝트를 열고 File > Add Package Dependencies를 선택합니다.

 2. 다음 URL을 입력합니다:
    https://github.com/googlemaps/google-maps-ios-sdk

 3. 다음 패키지들을 추가합니다:
    - GoogleMaps
    - GooglePlaces

 4. Info.plist에 다음 권한을 추가합니다:
    - NSLocationWhenInUseUsageDescription
    - NSLocationAlwaysAndWhenInUseUsageDescription

 5. GoogleMapsService.swift 파일에서 API 키를 설정합니다:
    GMSServices.provideAPIKey("YOUR_API_KEY")

 6. Google Cloud Console에서 Maps SDK for iOS를 활성화하고 API 키를 생성합니다.
*/
